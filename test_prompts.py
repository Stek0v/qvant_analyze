ALL_SETS = {"A": SET_A, "B": SET_B, "C": SET_C}


def get_prompts_for_phase(phase: int) -> list[tuple[str, int, str]]:
    """Вернуть список (set_name, index, prompt_text) для фазы.

    Фазы 1-2: по 5 из каждого набора (15 итого).
    Фаза 3: 10 из набора C.
    Фаза 4: все 20 из A+B.
    """
    if phase in (1, 2):
        prompts = []
        for name, s in ALL_SETS.items():
            for i in range(min(5, len(s))):
                prompts.append((name, i, s[i]))
        return prompts
    elif phase == 3:
        return [("C", i, SET_C[i]) for i in range(len(SET_C))]
    elif phase == 4:
        prompts = []
        for name in ("A", "B"):
            for i, p in enumerate(ALL_SETS[name]):
                prompts.append((name, i, p))
        return prompts
    return []
